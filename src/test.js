const contract = new ethers.Contract(contractAddress, contractABI, provider);

contract.on("CertificateMinted", async (student, tokenId, tokenURI, event) => {
    console.log(`Event received!`);
    console.log(`event: ${event}`);
    console.log(`Student: ${student}`);
    console.log(`Token ID: ${tokenId}`);
    console.log(`tokenURI: ${tokenURI}`);

    // 检查 tokenId 的类型
    console.log(`Type of tokenId: ${typeof tokenId}`);
    console.log(`tokenId instanceof BigNumber: ${tokenId instanceof ethers.BigNumber}`);

    // 将 tokenId 转换为字符串
    const tokenIdStr = tokenId.toString();
    console.log(`tokenId as string: ${tokenIdStr}`);

    // 调用 API 接口
    try {
        const response = await axios.post('http://localhost:3010/api/nft', {
            tokenURI: tokenURI,
            tokenId: tokenIdStr,
            userAddress: student
        });
        console.log('API response:', response.data);
    } catch (error) {
        console.error('Error calling API:', error);
    }
});